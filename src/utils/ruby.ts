// 检查字符是否为日文汉字
function isJapaneseKanji(char: string): boolean {
  return /[\u4e00-\u9fff\u3400-\u4dbf]/.test(char)
}

// 简单分词（基于词典正向最大匹配）
function tokenize(text: string): string[] {
  const tokens: string[] = []
  let i = 0
  
  while (i < text.length) {
    let matched = false
    
    // 尝试匹配最长词（从5个字开始）
    for (let len = Math.min(5, text.length - i); len >= 1; len--) {
      const word = text.slice(i, i + len)
      
      // 跳过非汉字
      if (!isJapaneseKanji(word[0])) {
        break
      }
      
      if (wordDictionary[word]) {
        tokens.push(word)
        i += len
        matched = true
        break
      }
    }
    
    if (!matched) {
      // 单字符
      tokens.push(text[i])
      i++
    }
  }
  
  return tokens
}

// 将文本转换为 RubyWord 数组
export function generateRuby(text: string): RubyWord[] {
  const tokens = tokenize(text)
  const rubyWords: RubyWord[] = []
  
  for (const token of tokens) {
    let kana = ''
    
    if (isJapaneseKanji(token[0])) {
      // 是汉字，查找假名
      if (wordDictionary[token]) {
        kana = wordDictionary[token]
      } else if (basicDictionary[token]) {
        kana = basicDictionary[token]
      } else {
        // 找不到转换，保留原字符
        kana = token
      }
    } else {
      // 非汉字（假名、数字等）
      kana = token
    }
    
    rubyWords.push({
      word: token,
      kana
    })
  }
  
  return rubyWords
}

// 解析文章内容，返回带注音的 ContentBlock 数组
export function parseArticleContent(
  content: string | ContentBlock[],
  autoRuby: boolean = false
): ContentBlock[] {
  // 如果已经是数组格式
  if (Array.isArray(content)) {
    return content
  }
  
  // 如果是字符串且需要自动注音
  if (autoRuby && typeof content === 'string') {
    const lines = content.split('\n')
    return lines.map(line => ({
      text: line,
      ruby: generateRuby(line)
    }))
  }
  
  // 如果是字符串且不需要自动注音，返回纯文本块
  if (typeof content === 'string') {
    return content.split('\n').map(line => ({
      text: line
    }))
  }
  
  return []
}
